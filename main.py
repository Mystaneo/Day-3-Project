question1 = input("As you travel you see a fork in the road. Do you turn left or right?\n")
if question1.lower() == "left":
  question2 = input("As you proceed, you approach the water's edge. Do you swim or wait?\n")
  if question2.lower() == "wait":
    question3 = input("Wet and tired you arrive at three doors colored red, blue, and yellow. Which color do you open?\n")
    if question3.lower() == "yellow":
      print("You win! Enjoy your treasure!")
    elif question3.lower() == "red":
      print("A wild tiger eats you. Game Over.")
    elif question3.lower() == "blue":
      print("You slip back into the water and drown. Game Over.")
  else:
    print("You are swarmed by killer crabs. Game Over.")
else:
  print("You step on a bear trap. Game Over.")          